  <h3>1.1.4.2 Foreign Travel (Continuation)</h3>

  <p>
    • Heads of GOCCs and GFIs under or attached to the Office of the President<br>
    • Heads of agencies under or attached to the Office of the President (OP)
  </p>

  <p>
    The Chief Justice and Associate Justices of the Supreme Court were exempted under Memorandum Order No. 26 dated July 31, 1986. Under EO No. 477 dated August 21, 1991, the Chairmen and Commissioners of the Constitutional Commissions, Chairman and Members of the Commission on Human Rights, Ombudsman and Deputy Ombudsman were also exempted from securing prior approval from the Office of the President in connection with travels abroad.
  </p>

  <p>
    As approved by the respective heads of agencies in the case of other government officials and employees regardless of the length of travel:
  </p>

  <ul>
    <li>National agencies – Department Secretaries or their equivalents</li>
    <li>GOCCs and GFIs attached to the OP – Heads of the GOCCs or GFIs</li>
    <li>GOCCs and GFIs not attached to the OP – Department Heads to which they are attached</li>
    <li>Provincial Governors and Mayors of highly urbanized cities or independent component cities – Secretary of the Department of the Interior and Local Government</li>
    <li>State Universities and Colleges (SUCs) – Chairman of Commission on Higher Education (CHED) in the case of heads of the SUCs and respective heads in the case of all other officials and employees</li>
    <li>Technical and Vocational Schools – Chairman of Technical Education and Skills Development Authority (TESDA) in the case of heads of technical and vocational schools and respective heads in the case of all other officials and employees</li>
  </ul>

