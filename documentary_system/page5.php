    <h3>1.2 Liquidation of Cash Advances</h3>

    <h4>General Guidelines</h4>
    <p>The accountable officer shall liquidate cash advances within the following period:</p>

    <ul>
      <li>Salaries, Wages, Allowances, Honoraria and Other Similar Payments – within five calendar days after the end of the pay period</li>
      <li>Field Operating Expenses – within 20 calendar days after the end of the year subject to replenishment as frequently as necessary during the year</li>
      <li>Petty Cash Fund (PCF) – as soon as its disbursements reach 75 percent or as needed, the PCF shall be replenished which shall be equal to the total amount of expenditures made therefrom. In case of termination, resignation, retirement or dismissal of the PCF custodian, immediately thereafter.</li>
      <li>Traveling Expenses – within 30 days after the return of the official/employee concerned to his official station for local travel and within 60 days after the return of the official/employee concerned to the Philippines in the case of foreign travel.</li>
      <li>Special purpose – as soon as the purpose of the cash advance has been served.</li>
    </ul>

