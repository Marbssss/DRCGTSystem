  <h2>DOCUMENTARY REQUIREMENTS<br>FOR COMMON GOVERNMENT TRANSACTIONS</h2>

  <h4>Documentary Requirements common to all cash advances except for travels</h4>
  <ul class="checkbox">
    <li><input type="checkbox"> Authority of the accountable officer issued by the Head of the Agency or his duly authorized representative indicating the maximum accountability and purpose of cash advance (for initial cash advance)</li>
    <li><input type="checkbox"> Certification from the Accountant that previous cash advances have been liquidated and accounted for in the books</li>
    <li><input type="checkbox"> Approved application for bond and/or Fidelity Bond for the year for cash accountability of ₱2,000 or more</li>
  </ul>

  <h4>1.1.1 Payroll Fund for Salaries, Wages, Allowances, Honoraria and Other Similar Expenses</h4>
  <p>
    The cash advance for payroll fund shall be equal to the net amount of the payroll for the pay period.
  </p>

  <p><em>Additional Documentary Requirements:</em></p>
  <ul class="checkbox">
    <li><input type="checkbox"> Approved contracts (for initial payment)</li>
    <li><input type="checkbox"> Approved Payroll or list of payees indicating their net payments</li>
    <li><input type="checkbox"> Approval/authority (presidential directive or legislative enactment) or legal basis to pay any allowance/salaries/wages/fringe benefits</li>
    <li><input type="checkbox"> Daily time record (DTR) approved by the supervisor</li>
  </ul>

  <h4>1.1.2 Petty Cash Fund (PCF)</h4>
  <p>
    The PCF to be set up shall be sufficient for the recurring petty operating expenses of the agency for one month. The cash advance shall not be used for payment of regular expenses, such as rentals, subscriptions, light and water bills, and the like. 
    Payments out of PCF, which shall be made through a Petty Cash Voucher, shall be allowed only for amounts not exceeding ₱15,000 for each transaction, except when a higher amount is allowed by law and/or specific authority by the Commission on Audit. 
    Splitting of transactions to avoid exceeding the ceiling shall not be allowed.
  </p>

  <p><em>Additional Documentary Requirements for initial cash advances:</em></p>
  <ul class="checkbox">
    <li><input type="checkbox"> Approved estimates of petty expenses for one month</li>
    <li><input type="checkbox"> Copy of policy for maintaining PCF under the imprest system for GOCCs</li>
  </ul>

  <h4>1.1.3 Field/Activity Current Operating Expenses (COE)</h4>
  <p>
    The amount of the cash advance shall be limited to the requirements for two months. Additional cash advances shall be granted on the basis of the activity budget or the requirements for two months, whichever is lower.
  </p>

  <p><em>Additional Documentary Requirement:</em></p>
  <ul class="checkbox">
    <li><input type="checkbox"> Approved Budget for COE of the agency field office or agency activity in the field</li>
  </ul>

