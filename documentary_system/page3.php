  <h3>1.1.4 Traveling Allowances</h3>

  <h4>General Guidelines</h4>
  <p>
    Under Section 2, Executive Order (EO) No. 248 dated May 29, 1995 as amended by EO No. 248-A dated August 14, 1995 and EO No. 298 dated March 23, 2004, travels shall cover only those that are urgent and extremely necessary, will involve the minimum expenditure and are beneficial to the agency concerned and/or the country.
  </p>

  <p>
    No government fund shall be utilized to defray foreign travel expenses of any government official or employee, except in the case of training, seminar or conference abroad when the officials or other personnel of the foreign mission cannot effectively represent the country therein, and travels necessitated by international commitments; provided that no official or employee, including uniformed personnel of the Department of the Interior and Local Government (DILG) and Department of National Defense (DND) will be sent to foreign training, conferences or attend international commitments when they are due to retire within one year after the said foreign travel [Section 16(c) of Fiscal Year (FY) 2012 General Appropriations Act (GAA) or pertinent provisions of the GAA for the year].
  </p>

  <p>
    Under Memorandum Circular No. 52 dated October 2, 2003 of the Office of the President, the grant of clothing allowance in all categories of trips is suspended indefinitely.
  </p>

  <p>
    Traveling allowances granted shall be treated and accounted for as cash advances. No additional cash advance shall be granted to any official or employee unless the previous cash advance given to him for travel is first liquidated and accounted for in the books as stated under Item 4.1.3(i) of COA Circular No. 2009-002 dated May 18, 2009. The guidelines on the grant of traveling allowances are prescribed under COA Circular No. 96-004 dated April 19, 1996.
  </p>

